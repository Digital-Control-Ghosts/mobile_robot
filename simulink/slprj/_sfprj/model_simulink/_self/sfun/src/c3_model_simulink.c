static void sf_gateway_c3_model_simulink(SFc3_model_simulinkInstanceStruct
  *chartInstance)
{
  real_T c3_hoistedGlobal;
  real_T c3_b_hoistedGlobal;
  real_T c3_c_hoistedGlobal;
  real_T c3_b_VR;
  real_T c3_b_VL;
  real_T c3_b_Theta;
  uint32_T c3_debug_family_var_map[10];
  real_T c3_L;
  real_T c3_v;
  real_T c3_nargin = 3.0;
  real_T c3_nargout = 3.0;
  real_T c3_b_Ydot;
  real_T c3_b_Xdot;
  real_T c3_b_w;
  real_T c3_A;
  real_T c3_x;
  real_T c3_b_x;
  real_T c3_c_x;
  real_T c3_b_A;
  real_T c3_d_x;
  real_T c3_e_x;
  real_T c3_f_x;
  real_T c3_u;
  const mxArray *c3_y = NULL;
  real_T c3_g_x;
  real_T c3_h_x;
  real_T c3_i_x;
  real_T c3_j_x;
  _SFD_SYMBOL_SCOPE_PUSH(0U, 0U);
  _sfTime_ = sf_get_time(chartInstance->S);
  _SFD_CC_CALL(CHART_ENTER_SFUNCTION_TAG, 2U, chartInstance->c3_sfEvent);
  _SFD_DATA_RANGE_CHECK(*chartInstance->c3_VR, 0U);
  chartInstance->c3_sfEvent = CALL_EVENT;
  _SFD_CC_CALL(CHART_ENTER_DURING_FUNCTION_TAG, 2U, chartInstance->c3_sfEvent);
  c3_hoistedGlobal = *chartInstance->c3_VR;
  c3_b_hoistedGlobal = *chartInstance->c3_VL;
  c3_c_hoistedGlobal = *chartInstance->c3_Theta;
  c3_b_VR = c3_hoistedGlobal;
  c3_b_VL = c3_b_hoistedGlobal;
  c3_b_Theta = c3_c_hoistedGlobal;
  _SFD_SYMBOL_SCOPE_PUSH_EML(0U, 10U, 10U, c3_debug_family_names,
    c3_debug_family_var_map);
  _SFD_SYMBOL_SCOPE_ADD_EML(&c3_L, 0U, c3_sf_marshallOut);
  _SFD_SYMBOL_SCOPE_ADD_EML_IMPORTABLE(&c3_v, 1U, c3_sf_marshallOut,
    c3_sf_marshallIn);
  _SFD_SYMBOL_SCOPE_ADD_EML_IMPORTABLE(&c3_nargin, 2U, c3_sf_marshallOut,
    c3_sf_marshallIn);
  _SFD_SYMBOL_SCOPE_ADD_EML_IMPORTABLE(&c3_nargout, 3U, c3_sf_marshallOut,
    c3_sf_marshallIn);
  _SFD_SYMBOL_SCOPE_ADD_EML(&c3_b_VR, 4U, c3_sf_marshallOut);
  _SFD_SYMBOL_SCOPE_ADD_EML(&c3_b_VL, 5U, c3_sf_marshallOut);
  _SFD_SYMBOL_SCOPE_ADD_EML(&c3_b_Theta, 6U, c3_sf_marshallOut);
  _SFD_SYMBOL_SCOPE_ADD_EML_IMPORTABLE(&c3_b_Ydot, 7U, c3_sf_marshallOut,
    c3_sf_marshallIn);
  _SFD_SYMBOL_SCOPE_ADD_EML_IMPORTABLE(&c3_b_Xdot, 8U, c3_sf_marshallOut,
    c3_sf_marshallIn);
  _SFD_SYMBOL_SCOPE_ADD_EML_IMPORTABLE(&c3_b_w, 9U, c3_sf_marshallOut,
    c3_sf_marshallIn);
  CV_EML_FCN(0, 0);
  _SFD_EML_CALL(0U, chartInstance->c3_sfEvent, 3);
  c3_L = 0.15;
  _SFD_EML_CALL(0U, chartInstance->c3_sfEvent, 4);
  c3_A = c3_b_VR + c3_b_VL;
  c3_x = c3_A;
  c3_b_x = c3_x;
  c3_c_x = c3_b_x;
  c3_v = c3_c_x / 2.0;
  _SFD_EML_CALL(0U, chartInstance->c3_sfEvent, 5);
  c3_b_A = c3_b_VR - c3_b_VL;
  c3_d_x = c3_b_A;
  c3_e_x = c3_d_x;
  c3_f_x = c3_e_x;
  c3_b_w = c3_f_x / 0.15;
  sf_mex_printf("%s =\\n", "w");
  c3_u = c3_b_w;
  c3_y = NULL;
  sf_mex_assign(&c3_y, sf_mex_create("y", &c3_u, 0, 0U, 0U, 0U, 0), false);
  sf_mex_call_debug(sfGlobalDebugInstanceStruct, "disp", 0U, 1U, 14, c3_y);
  _SFD_EML_CALL(0U, chartInstance->c3_sfEvent, 6);
  c3_g_x = c3_b_Theta;
  c3_h_x = c3_g_x;
  c3_h_x = muDoubleScalarCos(c3_h_x);
  c3_b_Xdot = c3_v * c3_h_x;
  _SFD_EML_CALL(0U, chartInstance->c3_sfEvent, 7);
  c3_i_x = c3_b_Theta;
  c3_j_x = c3_i_x;
  c3_j_x = muDoubleScalarSin(c3_j_x);
  c3_b_Ydot = c3_v * c3_j_x;
  _SFD_EML_CALL(0U, chartInstance->c3_sfEvent, -7);
  _SFD_SYMBOL_SCOPE_POP();
  *chartInstance->c3_Ydot = c3_b_Ydot;
  *chartInstance->c3_Xdot = c3_b_Xdot;
  *chartInstance->c3_w = c3_b_w;
  _SFD_CC_CALL(EXIT_OUT_OF_FUNCTION_TAG, 2U, chartInstance->c3_sfEvent);
  _SFD_SYMBOL_SCOPE_POP();
  _SFD_CHECK_FOR_STATE_INCONSISTENCY(_model_simulinkMachineNumber_,
    chartInstance->chartNumber, chartInstance->instanceNumber);
  _SFD_DATA_RANGE_CHECK(*chartInstance->c3_Ydot, 1U);
  _SFD_DATA_RANGE_CHECK(*chartInstance->c3_VL, 2U);
  _SFD_DATA_RANGE_CHECK(*chartInstance->c3_Xdot, 3U);
  _SFD_DATA_RANGE_CHECK(*chartInstance->c3_w, 4U);
  _SFD_DATA_RANGE_CHECK(*chartInstance->c3_Theta, 5U);
}

