  c3_u = sf_mex_dup(c3_st);
  *chartInstance->c3_Xdot = c3_emlrt_marshallIn(chartInstance, sf_mex_dup
    (sf_mex_getcell(c3_u, 0)), "Xdot");
  *chartInstance->c3_Ydot = c3_emlrt_marshallIn(chartInstance, sf_mex_dup
    (sf_mex_getcell(c3_u, 1)), "Ydot");
  *chartInstance->c3_w = c3_emlrt_marshallIn(chartInstance, sf_mex_dup
    (sf_mex_getcell(c3_u, 2)), "w");
